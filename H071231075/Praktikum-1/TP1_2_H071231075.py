c = float(input("masukkan nilai celcius :"))
celcius = int(c)

kelvin  = celcius + 273
fahrenheit = (celcius * 9/5) + 32
reamur = 4/5 * celcius

print(f"hasil konversi dari suhu {celcius} derajat celcius ke kelvin : {kelvin}K")
print(f"hasil konversi dari suhu {celcius} derajat celcius ke fahrenheit : {int(fahrenheit)}F")
print(f"hasil konversi dari suhu {celcius} derajat celcius ke kelvin : {int(reamur)}R")


